// @desc    Block user
// @route   PUT /api/users/:id/block
// @access  Private/Admin
exports.blockUser = async (req, res, next) => {
  try {
    const user = await User.findByIdAndUpdate(
      req.params.id,
      { isBlocked: true },
      {
        new: true,
        runValidators: true
      }
    );

    if (!user) {
      return res.status(404).json({
        success: false,
        error: 'User not found'
      });
    }
    
    // Log activity
    await Activity.create({
      admin: req.user.id,
      action: user.role === 'seller' ? 'BLOCK_SELLER' : 'BLOCK_USER',
      target: user._id,
      targetType: user.role === 'seller' ? 'SELLER' : 'USER'
    });

    res.status(200).json({
      success: true,
      data: user,
      message: 'User has been blocked successfully'
    });
  } catch (err) {
    res.status(500).json({
      success: false,
      error: 'Server Error'
    });
  }
};

